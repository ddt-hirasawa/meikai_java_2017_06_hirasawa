package e_05_03;

public class e_05_03 {

	public static void main(String[] args) {
		
		boolean test_bool_false = false;					//論理型の値を false で初期化
		boolean test_bool_true= true;						//論理型の値を true で初期化
		
		//論理値を表示させる false
		System.out.println("論理値 true  : " + test_bool_true);
		//論理値を表示させる true
		System.out.println("論理値 false : " + test_bool_false);	
	}
}
