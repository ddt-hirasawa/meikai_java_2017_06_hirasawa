package e_07_05;

public class e_07_05 {

	public static void main(String[] args) {

		//メソッド呼び出し部分
		hello();
	}
	
	//メソッド こんにちわと表示します
	//引数 無し
	//返却値 なし
	static void hello() {
		
		//メソッド部分でこんにちは
		System.out.println("こんにちは");
	}
}
